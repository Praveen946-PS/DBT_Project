-- Check task status
SHOW TASKS IN CDW_TEST.PUBLIC;

-- Check stream status
SELECT SYSTEM$STREAM_HAS_DATA('CDW_TEST.BRONZE.STM_RAW_VEEVA_CALLS') AS VEEVA_HAS_DATA,
       SYSTEM$STREAM_HAS_DATA('CDW_TEST.BRONZE.STM_RAW_HCP') AS HCP_HAS_DATA;

-- Check row counts across layers
SELECT 'BRONZE - RAW_VEEVA_CALLS' AS LAYER, COUNT(*) AS ROWS FROM CDW_TEST.BRONZE.RAW_VEEVA_CALLS
UNION ALL SELECT 'BRONZE - RAW_HCP', COUNT(*) FROM CDW_TEST.BRONZE.RAW_HCP
UNION ALL SELECT 'SILVER - SILVER_VEEVA_CALLS', COUNT(*) FROM CDW_TEST.SILVER.SILVER_VEEVA_CALLS
UNION ALL SELECT 'GOLD - FACT_CALL_ACTIVITY', COUNT(*) FROM CDW_TEST.GOLD.FACT_CALL_ACTIVITY
UNION ALL SELECT 'GOLD - DIM_TIME', COUNT(*) FROM CDW_TEST.GOLD.DIM_TIME;

-- Recent query history
SELECT QUERY_ID, QUERY_TEXT, USER_NAME, START_TIME, EXECUTION_STATUS
FROM TABLE(CDW_TEST.INFORMATION_SCHEMA.QUERY_HISTORY(RESULT_LIMIT => 20))
ORDER BY START_TIME DESC;
