CREATE OR REPLACE SCHEMA CDW_DEV_TEST.REFERENCE;

CREATE OR REPLACE VIEW CDW_DEV_TEST.REFERENCE.V_PIPELINE_MONITOR AS
SELECT
    NAME AS TASK_NAME,
    STATE AS TASK_STATE,
    SCHEDULED_TIME,
    COMPLETED_TIME,
    DATEDIFF('SECOND', SCHEDULED_TIME, COMPLETED_TIME) AS DURATION_SEC,
    RETURN_VALUE,
    ERROR_CODE,
    ERROR_MESSAGE
FROM TABLE(CDW_DEV_TEST.INFORMATION_SCHEMA.TASK_HISTORY(
    SCHEDULED_TIME_RANGE_START => DATEADD('DAY', -7, CURRENT_TIMESTAMP()),
    RESULT_LIMIT => 100
))
WHERE NAME IN ('TASK_STAGING', 'TASK_SILVER', 'TASK_GOLD', 'TASK_CONSUME_STREAMS')
ORDER BY SCHEDULED_TIME DESC;
